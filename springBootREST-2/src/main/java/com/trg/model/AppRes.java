package com.trg.model;

public class AppRes {

	String mes;
	String code;
	public AppRes(String mes, String code) {
	super();
	this.mes = mes;
	this.code = code;
	}
	public String getMes() {
	return mes;
	}
	public void setMes(String mes) {
	this.mes = mes;
	}
	public String getCode() {
	return code;
	}
	public void setCode(String code) {
	this.code = code;
	}
}
